bool indp(vll &x,vll &y){
    if(x[0]==y[0])return x[1]==y[1];
    lli a=x[0]-y[0];
    lli b=x[1]-y[1];
    lli c=x[2]-y[2];
    lli d=b*b-4*a*c;
    return d<0;
}


