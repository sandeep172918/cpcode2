vector<lli> prim(lli x){
    vll v;
    for(lli i=2;i*i<=x;i++){
         if(x%i==0){
            v.psb(i);
            while(x%i==0)x/=i;
         }
    }
    if(x>1)v.psb(x);
    return v;
}


